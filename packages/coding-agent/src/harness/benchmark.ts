function ok(input: ChatGptWorkerCommand, stdout: string): ChatGptWorkerCommandResult {
	return { ok: true, action: input.action, exitCode: 0, stdout, stderr: "", command: ["mock-chatgpt", input.action] };
}

async function writeSkill(cwd: string, name: string): Promise<void> {
	const skillDir = path.join(cwd, ".omg", "chatgpt-skills", name);
	await fs.mkdir(skillDir, { recursive: true, mode: 0o700 });
	await Bun.write(
		path.join(skillDir, "SKILL.md"),
		[
			"---",
			`name: ${name}`,
			`description: ${name} benchmark skill`,
			"---",
			"Return the requested JSON artifact and follow local OMG validation as authoritative.",
			"Validation notes: use the supplied schema and validator before responding.",
		].join("\n"),
	);
}

async function zipDirectory(root: string, prefix = "workspace"): Promise<Uint8Array> {
	const entries: Record<string, Uint8Array> = {};
	async function walk(dir: string): Promise<void> {
		for (const entry of await fs.readdir(dir, { withFileTypes: true })) {
			const absolute = path.join(dir, entry.name);
			if (entry.isDirectory()) await walk(absolute);
			else if (entry.isFile()) {
				const rel = path.relative(root, absolute).replace(/\\/g, "/");
				entries[`${prefix}/${rel}`] = new Uint8Array(await Bun.file(absolute).arrayBuffer());
			}
		}
	}
	await walk(root);
	return zipSync(entries);
}

async function writeArtifactProjectWorkspace(root: string, options: { failingTest?: boolean } = {}): Promise<void> {
	await fs.mkdir(path.join(root, "src"), { recursive: true, mode: 0o700 });
	await fs.mkdir(path.join(root, "tests"), { recursive: true, mode: 0o700 });
	await Bun.write(path.join(root, "README.md"), "# Benchmark Artifact Project\n");
	await Bun.write(path.join(root, "PROJECT_REPORT.md"), "Benchmark project generated by OMG harness.\n");
	await Bun.write(path.join(root, "src", "app.py"), "def answer():\n    return 42\n");
	await Bun.write(
		path.join(root, "tests", "test_app.py"),
		options.failingTest
			? "import unittest\n\nclass AppTest(unittest.TestCase):\n    def test_fail(self):\n        self.assertEqual(1, 2)\n"
			: "import unittest\nfrom app import answer\n\nclass AppTest(unittest.TestCase):\n    def test_answer(self):\n        self.assertEqual(answer(), 42)\n",
	);
	await Bun.write(
		path.join(root, "PROJECT_MANIFEST.json"),
		`${JSON.stringify(
			{
				name: "benchmark-artifact-project",
				description: "Small Python project for OMG benchmark validation.",
				language: "Python",
				entrypoints: ["src/app.py"],
				test_command: "python -m unittest discover -s tests",
				expected_files: ["README.md", "PROJECT_REPORT.md", "src/app.py", "tests/test_app.py"],
				limitations: [],
			},
			null,
			2,
		)}\n`,
	);
}

function jsonFile(value: unknown): Uint8Array {
	return new TextEncoder().encode(`${JSON.stringify(value, null, 2)}\n`);
}

