function summarizeLiveRuns(runs: HarnessRunState[]): HarnessBenchmarkLiveSummary {
	const byTemplate: Record<string, number> = {};
	const byStatus: Record<string, number> = {};
	const latestGoodEnoughByTemplate: Record<string, string> = {};
	const failedGates = new Map<string, number>();
	for (const run of runs) {
		const template = normalizeHarnessTemplate(run.template) ?? "(none)";
		byTemplate[template] = (byTemplate[template] ?? 0) + 1;
		byStatus[run.status] = (byStatus[run.status] ?? 0) + 1;
		if (run.status === "good_enough" && !latestGoodEnoughByTemplate[template]) {
			latestGoodEnoughByTemplate[template] = run.runId;
		}
		for (const gate of run.gates ?? []) {
			if (gate.status === "failed") failedGates.set(gate.id, (failedGates.get(gate.id) ?? 0) + 1);
		}
	}
	return {
		totalRuns: runs.length,
		byTemplate,
		byStatus,
		latestGoodEnoughByTemplate,
		commonFailedGates: [...failedGates.entries()]
			.map(([gate, count]) => ({ gate, count }))
			.sort((a, b) => b.count - a.count || a.gate.localeCompare(b.gate)),
	};
}

async function writeBenchmarkReport(result: HarnessBenchmarkResult): Promise<string> {
	const lines = [
		`# Harness Benchmark ${result.benchmarkId}`,
		"",
		`- Schema: ${result.schemaVersion}`,
		`- Template: ${result.template}`,
		`- Result: ${result.ok ? "pass" : "fail"}`,
		`- Created: ${result.createdAt}`,
		"",
		"## Scenarios",
		"",
		"| Scenario | Template | Result | Prompts | Repairs | Downloads | Status | Blocker |",
		"| --- | --- | --- | ---: | ---: | ---: | --- | --- |",
		...result.scenarios.map(
			scenario =>
				`| ${scenario.id} | ${scenario.template} | ${scenario.ok ? "pass" : "fail"} | ${scenario.promptBudget.used}/${scenario.promptBudget.expectedMax} | ${scenario.repairPrompts} | ${scenario.artifactDownloadAttempts} | ${scenario.actualStatus ?? "(none)"} | ${scenario.blocker ?? ""} |`,
		),
		"",
	];
	if (result.liveRuns) {
		lines.push(
			"## Existing Live Run Summary",
			"",
			`- Total runs: ${result.liveRuns.totalRuns}`,
			`- By template: ${JSON.stringify(result.liveRuns.byTemplate)}`,
			`- By status: ${JSON.stringify(result.liveRuns.byStatus)}`,
			`- Latest good_enough by template: ${JSON.stringify(result.liveRuns.latestGoodEnoughByTemplate)}`,
			`- Common failed gates: ${JSON.stringify(result.liveRuns.commonFailedGates)}`,
			"",
		);
	}
	await Bun.write(result.reportPath, `${lines.join("\n")}\n`);
	return result.reportPath;
}

export async function runHarnessBenchmark(options: HarnessBenchmarkOptions = {}): Promise<HarnessBenchmarkResult> {
	const normalizedTemplate = normalizeHarnessTemplate(options.template);
	const template: HarnessBenchmarkTemplate =
		options.template === "all" || !options.template ? "all" : (normalizedTemplate ?? "all");
	if (options.template && options.template !== "all" && !normalizedTemplate) {
		throw new Error("supported benchmark templates: artifact-project, wiki, all");
	}
	const id = benchmarkId();
	const benchmarkDir = path.join(getHarnessBenchmarksDir(options.agentDir), id);
	const workDir = path.join(benchmarkDir, "work");
	await fs.mkdir(workDir, { recursive: true, mode: 0o700 });
	const scenarios: HarnessBenchmarkScenarioResult[] = [];
	if (template === "artifact-project" || template === "all") {
		scenarios.push(...(await runArtifactScenarios(workDir)));
	}
	if (template === "wiki" || template === "all") {
		scenarios.push(...(await runWikiScenarios(workDir)));
	}
	const result: HarnessBenchmarkResult = {
		schemaVersion: HARNESS_BENCHMARK_SCHEMA_VERSION,
		benchmarkId: id,
		createdAt: new Date().toISOString(),
		template,
		ok: scenarios.every(scenario => scenario.ok),
		reportPath: path.join(benchmarkDir, "report.md"),
		benchmarkPath: path.join(benchmarkDir, "benchmark.json"),
		scenarios,
		liveRuns: options.includeLiveRuns ? summarizeLiveRuns(await listHarnessRuns(options.agentDir)) : undefined,
	};
	await fs.mkdir(benchmarkDir, { recursive: true, mode: 0o700 });
	await Bun.write(result.benchmarkPath, `${JSON.stringify(result, null, 2)}\n`);
	await writeBenchmarkReport(result);
	return result;
}
